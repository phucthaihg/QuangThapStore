class ProductService {
    constructor() {}

    getProductById(id) {

        return products[id];
    }

    getProductsByCategory(category) {
        const filteredProducts = Object.values(products).filter(product => {
            return product.category.toLowerCase() === category.toLowerCase()
        });

        return filteredProducts;
    }

    getAllProducts() {
        const allProducts = Object.values(products);

        return allProducts;
    }

    searchProducts(query) {
        query = query.toLowerCase();
        return this.getAllProducts().filter(product => 
            product.name.toLowerCase().includes(query) || 
            product.category.toLowerCase().includes(query)
        );
    }

    createProductCard(product) {
        return `
            <div class="card h-100">
                <a href="product.html?id=${product.id}" class="text-decoration-none">
                    <img src="${product.images[0]}" class="card-img-top" alt="${product.name}">
                </a>
                <div class="card-body d-flex flex-column">
                    <h5 class="card-title">${product.name}</h5>
                    <p class="card-text">${product.shortDescription}</p>
                    <p class="card-text mt-auto"><strong>$${product.price}</strong></p>
                    <button class="btn btn-primary mt-2 add-to-cart" data-product-id="${product.id}">Add to Cart</button>
                    <button class="btn btn-outline-secondary mt-2 add-to-wishlist" data-product-id="${product.id}">Add to Wishlist</button>
                </div>
            </div>
        `;
    }
}